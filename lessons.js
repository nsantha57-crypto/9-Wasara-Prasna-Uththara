function getRandomDistractors(count, exclude) {
  let shuffled = [...genericDistractors].sort(() => 0.5 - Math.random());
  let res = [];
  for (let d of shuffled) {
    if (d !== exclude && res.length < count) res.push(d);
  }
  return res;
}

function processMCQs(questionsArr) {
  return questionsArr.map(qObj => {
    let distractors = getRandomDistractors(3, qObj.a);
    let options = [qObj.a, ...distractors];
    let shuffledOptions = options.sort(() => Math.random() - 0.5);
    let correctIndex = shuffledOptions.indexOf(qObj.a);
    return { q: qObj.q, options: shuffledOptions, correctIndex: correctIndex };
  });
}

